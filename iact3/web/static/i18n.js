let _lang = localStorage.getItem('iact3-lang') || (navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en');

function t(key, vars) {
    let str = I18N[_lang]?.[key] ?? I18N.en[key] ?? key;
    if (vars) {
        for (const [k, v] of Object.entries(vars)) str = str.replace(`{${k}}`, v);
    }
    return str;
}

function tStackStatus(status) {
    if (!status) return '';
    const map = I18N[_lang]?.stackStatusMap ?? I18N.en.stackStatusMap ?? {};
    return map[status] ?? status;
}

/**
 * Extract the actual error message from a status_reason string.
 * Strips prefixes like "Resource CREATE failed:", stack name, etc.
 * Returns the meaningful error detail.
 */
function extractErrorMessage(reason) {
    if (!reason) return '';
    let msg = reason.trim();
    // Terraform format: strip "ResultReason:" prefix and show everything after
    const rrMatch = msg.match(/ResultReason\s*:\s*(.*)$/s);
    if (rrMatch) {
        msg = rrMatch[1].trim();
        return translateStatusReason(msg);
    }
    // ROS format: strip "Resource CREATE/UPDATE/DELETE failed:" prefix and take the rest
    const m = msg.match(/Resource (?:CREATE|UPDATE|DELETE) failed\s*:\s*(.*)$/s);
    if (m) {
        msg = m[1].trim();
    }
    return translateStatusReason(msg);
}

/**
 * Translate common English patterns in ROS status_reason to Chinese.
 * Only applies when language is zh; English is returned as-is.
 */
function translateStatusReason(reason) {
    if (!reason) return '';
    if (_lang !== 'zh') return reason;
    let r = reason;
    // Error type prefixes
    r = r.replace(/StackValidationFailed/g, '资源栈校验失败');
    r = r.replace(/TemplateValidationFailed/g, '模板校验失败');
    r = r.replace(/TemplateFormatError/g, '模板格式错误');
    r = r.replace(/QuotaExceeded/g, '配额超限');
    r = r.replace(/Throttling/g, '请求被限流');
    r = r.replace(/InternalError/g, '内部错误');
    r = r.replace(/ResourceNotFound/g, '资源不存在');
    r = r.replace(/StackNotFound/g, '资源栈不存在');
    r = r.replace(/StackExists/g, '资源栈已存在');
    // Common phrases
    r = r.replace(/code:\s*(\d+)/gi, '状态码: $1');
    r = r.replace(/Property error/gi, '属性错误');
    r = r.replace(/Resource CREATE failed/gi, '资源创建失败');
    r = r.replace(/Resource UPDATE failed/gi, '资源更新失败');
    r = r.replace(/Resource DELETE failed/gi, '资源删除失败');
    r = r.replace(/request id:/gi, '请求ID:');
    r = r.replace(/Unknown error/g, '未知错误');
    return r;
}

function setLang(lang) {
    _lang = lang;
    localStorage.setItem('iact3-lang', lang);
    document.querySelectorAll('.lang-btn, .topbar-lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
    renderAll();
    window.dispatchEvent(new CustomEvent('iact3-lang-change', { detail: lang }));
}

function renderAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
}
